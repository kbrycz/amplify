// Helper function to get questions for a specific category and subcategory
export function getQuestionsForSubcategory(category, subcategoryId) {
  if (!category || !subcategoryId) {
    console.log('getQuestionsForSubcategory: Missing category or subcategoryId', { category, subcategoryId });
    return [];
  }
  
  try {
    const questions = campaignQuestions.categories[category]?.subcategories[subcategoryId]?.questions || [];
    console.log('getQuestionsForSubcategory: Found questions', { category, subcategoryId, questions });
    return questions;
  } catch (error) {
    console.error("Error getting questions:", error);
    return [];
  }
}

// Helper function to get subcategory name
export function getSubcategoryName(category, subcategoryId) {
  if (!category || !subcategoryId) {
    console.log('getSubcategoryName: Missing category or subcategoryId', { category, subcategoryId });
    return "";
  }
  
  try {
    const name = campaignQuestions.categories[category]?.subcategories[subcategoryId]?.name || "";
    console.log('getSubcategoryName: Found name', { category, subcategoryId, name });
    return name;
  } catch (error) {
    console.error("Error getting subcategory name:", error);
    return "";
  }
}

// Debug function to check if a subcategory exists
export function debugCampaignQuestions(category, subcategoryId) {
  console.log('Debug Campaign Questions:');
  console.log('- Category:', category);
  console.log('- Subcategory ID:', subcategoryId);
  
  if (!category) {
    console.log('- Error: Category is missing');
    return false;
  }
  
  if (!campaignQuestions.categories[category]) {
    console.log('- Error: Category does not exist in campaignQuestions');
    console.log('- Available categories:', Object.keys(campaignQuestions.categories));
    return false;
  }
  
  if (!subcategoryId) {
    console.log('- Error: Subcategory ID is missing');
    return false;
  }
  
  if (!campaignQuestions.categories[category].subcategories[subcategoryId]) {
    console.log('- Error: Subcategory does not exist in category');
    console.log('- Available subcategories:', Object.keys(campaignQuestions.categories[category].subcategories));
    return false;
  }
  
  console.log('- Success: Subcategory exists');
  console.log('- Subcategory name:', campaignQuestions.categories[category].subcategories[subcategoryId].name);
  console.log('- Questions:', campaignQuestions.categories[category].subcategories[subcategoryId].questions);
  return true;
} 